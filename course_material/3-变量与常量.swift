// 变量
var name = "Bob"
var anotherName = "Xia"
// variable
print(name)

name = anotherName
print(name)

// 常量
let myName = "X"
print(myName)

// 大小写是不一样的
let MYNAME = "Xuezheng"
print(MYNAME)

// 名字必须是唯一的
let hisName = "Sam"

// 不能有空白
// 不能有数学符号，箭头
// 不能用数字开头
let pet1 = "Kat"
let pet2 = "Doug"
print(pet1)
print(pet2)

// 骆驼式拼写
let mySecondCar = "McLaren"

